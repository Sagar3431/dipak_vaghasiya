# Original list
my_list = [1, 2, 3, 4, 5]

# Reversing the list using slicing
reversed_list = my_list[::-1]

# Printing the reversed list
print(reversed_list)


# Original list
my_list = [1, 2, 3, 4, 5]

# Reversing the list in place using the reverse() method
my_list.reverse()

# Printing the reversed list
print(my_list)

