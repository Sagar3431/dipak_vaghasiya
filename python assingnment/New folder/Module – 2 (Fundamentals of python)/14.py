'''
What are negative indexes and why are they used?
Ans:allows you to access elements of a sequence from the end, using negative numbers as indexes.
'''

# Define a list containing integers from 1 to 5
my_list = [1, 2, 3, 4, 5]

# Print the last element of the list using negative indexing
print("Last element:", my_list[-1])

# Print the second-to-last element of the list using negative indexing
print("Second-to-last element:", my_list[-2])

