'''
Write a Python program to create a tuple with different data types.
'''

# Create a tuple with different data types
mixed_tuple = (10, "hello", 3.14, True)

# Print the tuple
print("Mixed Tuple:", mixed_tuple)


print("First element:", mixed_tuple[0])   # Output: 10
print("Second element:", mixed_tuple[1])  # Output: hello
print("Third element:", mixed_tuple[2])   # Output: 3.14
print("Fourth element:", mixed_tuple[3])  # Output: True
